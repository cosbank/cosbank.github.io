function enable1() {
        var btn = $('#disabled1')
        let dombtn = document.getElementById('disabled1')
        dombtn.setAttribute('onclick', 'confirm1();');
        btn.removeClass('text-danger')
        btn.addClass('text-warning')
        btn.text('Confirm')
        btn.addId('confirm1')
        btn.removeId('disabled1')
      }
      function confirm1() {
        var btn = $('#disabled1')
        let dombtn = document.getElementById('disabled1')
        dombtn.setAttribute('onclick', 'disable1();');
        btn.removeClass('text-warning')
        btn.addClass('text-success')
        btn.text('Enabled')
        btn.addId('enabled1')
        btn.removeId('confirm1')
      }
      function disable1() {
        var btn = $('#disabled1')
        let dombtn = document.getElementById('disabled1')
        dombtn.setAttribute('onclick', 'enable1();');
        btn.removeClass('text-success')
        btn.addClass('text-danger')
        btn.text('Disabled')
        btn.addId('disabled1')
        btn.removeId('enabled1')
      }

